package dNumericTypes.Tarea;

public class Elefantes {
    public static void main(String[] args) {
        int elefantes = 1;

        while(elefantes <= 100){
            if(elefantes==1)
                System.out.println(elefantes + " elefante se columpiaba sobre la tela de una araña,\n" + "como veia que resistia, fue a llamar a otro elefante");
            else
                System.out.println(elefantes + " elefantes se columpiaban sobre la tela de una araña,\n" + "como veian que resistia, fueron a llamar a otro elefante");

            elefantes ++;
        }
    }
}
